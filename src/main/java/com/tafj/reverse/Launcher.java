package com.tafj.reverse;

/**
 * Launcher class for TAFJ Reverse Tool.
 * Provides a simple entry point that delegates to the Main class.
 */
public class Launcher {

    /**
     * Main entry point - delegates to Main class
     * @param args command line arguments
     */
    public static void main(String[] args) {
        Main.main(args);
    }
}
